
public class Main
{
	public static void main(String[] args) {
	    int counter = 100;
	    for(int i = counter; i >= -100; i--){
	        if (i%2!=0){
	            System.out.format("%d%%2 is odd\n",i);
	        }
	        else{
	        System.out.format("%d%%2 is even\n",i);
	        }
	            
        }
	}
}
